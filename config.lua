local config
config = require("lapis.config").config
config("moonrest", function()
  num_workers(8)
  return code_cache("on")
end)
return config("moonrest", function()
  postgres(function()
    host("127.0.0.1")
    user("postgres")
    password("postgres")
    return database("postgres")
  end)
  num_workers(1)
  code_cache("off")
  return postgresql_url("postgres://postgres:postgres@127.0.0.1/postgres")
end)
